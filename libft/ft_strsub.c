#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	unsigned int	i;
	char			*str;

	i = 0;
	str = ft_strnew(len);
	if (!str || !s)
		return (0);
	while (i++ < len)
		str[i - 1] = s[start++];
	return (str);
}
