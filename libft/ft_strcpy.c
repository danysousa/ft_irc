#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;

	if (dest != 0 || src != 0)
	{
		i = 0;
		while (src[i] != '\0')
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
		return (dest);
	}
	return (0);
}
